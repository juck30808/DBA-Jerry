CREATE TABLE Student(
			SNO INT PRIMARY KEY,
			SNAME CHAR(8) NOT NULL,
			SEX CHAR(2),
			DEPTNO INT
);

CREATE TABLE Course(
CNO INT,
CNAME CHAR(20) NOT NULL,
TNO INT,
CREDIT INT,
PRIMARY KEY(CNO,TNO)
);

CREATE TABLE SC (
SNO INT,
CNO INT,
PRIMARY KEY(SNO,CNO),
GRADE INT
);

CREATE TABLE Teacher(
TNO INT PRIMARY KEY,
TNAME CHAR(8) NOT NULL,
DEPTNO INT
);

CREATE TABLE Dept(
DEPTNO INT PRIMARY KEY,
DNAME CHAR(20) NOT NULL
);
		